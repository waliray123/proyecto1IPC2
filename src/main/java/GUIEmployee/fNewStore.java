    private void TableStoreMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableStoreMouseClicked
        int select = this.TableStore.rowAtPoint(evt.getPoint());
        
        this.TextFieldName.setText(String.valueOf(this.TableStore.getValueAt(select,0)));
        this.TextFieldAddress.setText(String.valueOf(this.TableStore.getValueAt(select,1)));
        this.TextFieldCode.setText(String.valueOf(this.TableStore.getValueAt(select,2)));
        this.TextFieldEMail.setText(String.valueOf(this.TableStore.getValueAt(select,3)));
        this.TextFieldPhone1.setText(String.valueOf(this.TableStore.getValueAt(select,4)));
        this.TextFieldPhone2.setText(String.valueOf(this.TableStore.getValueAt(select,5)));
        this.TextFieldShedule.setText(String.valueOf(this.TableStore.getValueAt(select,6)));

        this.codeStore = this.TextFieldCode.getText();
    }//GEN-LAST:event_TableStoreMouseClicked
      
    private boolean reviewRequiredFields(){
        this.setColorsLabels();
        boolean isReady = true;
        if (this.TextFieldName.getText().isEmpty() == true) {
            isReady= false;
            this.LabelStore.setForeground(Color.red);            
        }
        if (this.TextFieldCode.getText().isEmpty() == true) {
            isReady= false;
            this.LabelCode.setForeground(Color.red);
        }
        if (this.TextFieldAddress.getText().isEmpty() == true) {
            isReady= false;
            this.LabelAddress.setForeground(Color.red);
        }
        if (this.TextFieldPhone1.getText().isEmpty() == true) {
            isReady= false;
            this.LabelPhone1.setForeground(Color.red);
        }
        return isReady;
    }        
    
    private void setColorsLabels(){
        this.LabelCode.setForeground(Color.black);
        this.LabelStore.setForeground(Color.black);
        this.LabelPhone1.setForeground(Color.black);
        this.LabelAddress.setForeground(Color.black);
    }
    
    /**
     * This method is used by textfields so that the user does 
     * not enter any character that is not a digit.
     *
     * @param evt
    */
    public void validateOnlyNumbers(java.awt.event.KeyEvent evt){
        char validate = evt.getKeyChar();
        if (Character.isDigit(validate) == false) {
            evt.consume();
        }        
    }
    
    public void clearTextBox(){
        this.TextFieldAddress.setText("");
        this.TextFieldName.setText("");
        this.TextFieldCode.setText("");        
        this.TextFieldEMail.setText("");
        this.TextFieldPhone1.setText("");
        this.TextFieldPhone2.setText("");
        this.TextFieldShedule.setText("");
        
        this.codeStore = "";
    }
    
    public void setStores(){
        cleanTable();
        String code = "";
        String name = "";
        String address = "";
        String phone1 = "";
        String phone2 = "";
        String email = "";
        String shedule = "";
        
        int sizeStores = this.stores.size();
        for (int i = 0; i < sizeStores; i++) {
            code = this.stores.get(i).getCode();
            name = this.stores.get(i).getName();
            address = this.stores.get(i).getAddress();
            phone1 = this.stores.get(i).getPhone1();
            phone2 = this.stores.get(i).getPhone2();
            email = this.stores.get(i).getEmail();
            shedule = this.stores.get(i).getShedule();
                       
            this.dtmStore.addRow(new Object[]{name,address, code, email,phone1,phone2, shedule});
        }                
        
        this.TableStore.setModel(dtmStore);
    }
    
    public void cleanTable(){
        int filas=this.TableStore.getRowCount();
        for (int i = 0;filas>i; i++) {
            this.dtmStore.removeRow(0);
        }
    }
