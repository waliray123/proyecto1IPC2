    private void ButtonSaveEmployeeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ButtonSaveEmployeeActionPerformed
        if (this.reviewRequiredFields() == true ) {
            String code = this.TextFieldCode.getText();
            String name = this.TextFieldName.getText();
            String phone = this.TextFieldPhone.getText();                
            String NIT = this.TextFieldNit.getText();
            String DPI = this.TextFieldDpi.getText();
            String email = this.TextFieldEMail.getText();
            String address = this.TextFieldAddress.getText();
            if (this.codeEmployee.equals("")) 
                this.control.insertEmployee(code, name, phone, NIT, DPI, email, address);
            else
                this.control.updateEmployee(this.codeEmployee, name, phone, NIT, DPI, email, address);
            
            this.employees = this.control.setEmployee();
            setEmployees();            
            clearTextBox();
        }
    }//GEN-LAST:event_ButtonSaveEmployeeActionPerformed

    private void TableEmployeeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TableEmployeeMouseClicked
        int select = this.TableEmployee.rowAtPoint(evt.getPoint());
        
        this.TextFieldCode.setText(String.valueOf(this.TableEmployee.getValueAt(select,0)));
        this.TextFieldName.setText(String.valueOf(this.TableEmployee.getValueAt(select,1)));
        this.TextFieldDpi.setText(String.valueOf(this.TableEmployee.getValueAt(select,2)));
        this.TextFieldAddress.setText(String.valueOf(this.TableEmployee.getValueAt(select,3)));
        this.TextFieldPhone.setText(String.valueOf(this.TableEmployee.getValueAt(select,4)));
        this.TextFieldNit.setText(String.valueOf(this.TableEmployee.getValueAt(select,5)));
        this.TextFieldEMail.setText(String.valueOf(this.TableEmployee.getValueAt(select,6)));
        
        this.codeEmployee = this.TextFieldCode.getText();
    }//GEN-LAST:event_TableEmployeeMouseClicked

    private boolean reviewRequiredFields(){
        this.setColorsLabels();
        boolean isReady = true;
        if (this.TextFieldName.getText().isEmpty() == true) {
            isReady= false;
            this.LabelName.setForeground(Color.red);            
        }
        if (this.TextFieldPhone.getText().isEmpty() == true) {
            isReady= false;
            this.LabelPhone.setForeground(Color.red);
        }      
        if (this.TextFieldCode.getText().isEmpty() == true) {
            isReady= false;
            this.LabelCode.setForeground(Color.red);
        }
        if (this.TextFieldDpi.getText().isEmpty() == true) {
            isReady= false;
            this.LabelDPI.setForeground(Color.red);
        }
        if (this.TextFieldEMail.getText().isEmpty() == true) {
            isReady= false;
            this.LabelEMail.setForeground(Color.red);
        }
        if (this.TextFieldAddress.getText().isEmpty() == true) {
            isReady= false;
            this.LabelAddress.setForeground(Color.red);
        }
        
        return isReady;
    }
    
    private void setColorsLabels(){        
        this.LabelName.setForeground(Color.black);
        this.LabelPhone.setForeground(Color.black);        
        this.LabelCode.setForeground(Color.black);        
        this.LabelDPI.setForeground(Color.black);        
        this.LabelEMail.setForeground(Color.black);        
        this.LabelAddress.setForeground(Color.black);
        this.LabelNIT.setForeground(Color.black);
    }

    /**
     * This method is used by textfields so that the user does 
     * not enter any character that is not a digit.
     *
     * @param evt
    */
    public void validateOnlyNumbers(java.awt.event.KeyEvent evt){
        char validate = evt.getKeyChar();
        if (Character.isDigit(validate) == false) {
            evt.consume();
        }        
    }
    
    public void clearTextBox(){
        this.TextFieldCode.setText("");
        this.TextFieldAddress.setText("");
        this.TextFieldName.setText("");
        this.TextFieldNit.setText("");
        this.TextFieldDpi.setText("");
        this.TextFieldEMail.setText("");
        this.TextFieldPhone.setText("");
        this.codeEmployee = "";
    }
    
    public void setEmployees(){
        cleanTable();
        int code = 0;
        String name = "";
        String phone = "";                
        String NIT = "";
        String DPI = "";
        String email = "";
        String address = "";
        
        int sizeStores = this.employees.size();
        for (int i = 0; i < sizeStores; i++) {
            code = this.employees.get(i).getCode();
            name = this.employees.get(i).getName();            
            phone = this.employees.get(i).getPhone();
            NIT = this.employees.get(i).getNIT();            
            DPI = this.employees.get(i).getDPI();
            email = this.employees.get(i).getEmail();
            address = this.employees.get(i).getAddress();
                       
            this.dtmEmployee.addRow(new Object[]{code,name,DPI,address,phone,NIT,email});
        }                
        
        this.TableEmployee.setModel(dtmEmployee);
    }
    
    public void cleanTable(){
        int filas=this.TableEmployee.getRowCount();
        for (int i = 0;filas>i; i++) {
            this.dtmEmployee.removeRow(0);
        }
    }
    
