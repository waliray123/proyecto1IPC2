public class ShippingTime {
    private String code;
    private String StoreCodeOut;
    private String StoreCodeEnter;
    private String daysTime;

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getStoreCodeOut() {
        return StoreCodeOut;
    }

    public void setStoreCodeOut(String StoreCodeOut) {
        this.StoreCodeOut = StoreCodeOut;
    }

    public String getStoreCodeEnter() {
        return StoreCodeEnter;
    }

    public void setStoreCodeEnter(String StoreCodeEnter) {
        this.StoreCodeEnter = StoreCodeEnter;
    }

    public String getDaysTime() {
        return daysTime;
    }

    public void setDaysTime(String daysTime) {
        this.daysTime = daysTime;
    }
    
    
}
