    public boolean reviewCodeStore(ArrayList<Store> stores){
        codeStore = JOptionPane.showInputDialog("Escribe el codigo de la tienda");
        boolean exist = false;
        for (int i = 0; i < stores.size(); i++) {
            if (stores.get(i).getCode().equals(codeStore)) {
                exist = true;
                break;
            }            
        }        
        return exist;
    }

