    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        SimpleDateFormat sdf = new SimpleDateFormat("yyyy-MM-dd");
        this.date1 = sdf.format(this.jDateChooser1.getDate());
        this.date2 = sdf.format(this.jDateChooser2.getDate());
        if (this.typeReport == true) {
            Report7 report7 = new Report7(this.control, this.date1,this.date2);
            report7.setVisible(true);
        }else{
            ArrayList<Store> stores = this.control.setStores(); 
            if (reviewCodeStore(stores)) { 
                Report8 report8 = new Report8(this.control,this.date1,this.date2,this.codeStore);
                report8.setVisible(true);
            }else  {
                JOptionPane.showMessageDialog(null,"No existe esa tienda");
            }                        
        }
        this.dispose();
    }//GEN-LAST:event_jButton1ActionPerformed
    
